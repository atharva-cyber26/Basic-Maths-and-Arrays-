public class incrementoperatorex {
    public static void main(String[] args) {
        int a=1;

        int b=2;

        int c;
        int d;

        c=++b; // b is 3 and c is 3

        d=a++; //d is 1 but a is 2

        c++; // c is 4

        System.out.println(a);

        System.out.println(b);

        System.out.println(c);

        System.out.println(d);


    }
}
